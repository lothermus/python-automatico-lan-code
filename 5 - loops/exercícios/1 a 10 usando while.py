# Crie um programa que imprime os números de 10 até 1 usando um loop while.

contador = 10 # Primeiro é setado uma variável de contador

while contador > 0: #ENQUANTO o contador é menor ou igual que 10
    print(contador) # Imprime o contador (números)
    contador -= 1 # Adiciona 1 número no contador para não ficar infinito